#include<bits/stdc++.h>
using namespace std;
void result(long long a, long long b){
    
    cout<<a <<" + "<< b << " = " << a+b <<endl;
    cout<<a <<" * "<< b << " = " << a*b <<endl;
    cout<<a <<" - "<< b << " = " << a-b <<endl;
}
int main(){
    int a , b;
    cin>>a>>b;
    result(a,b);
    return 0;
}

